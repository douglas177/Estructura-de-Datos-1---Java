    private void AsignarValorFraccionC(){
        NumC.setText(String.valueOf(C.getNumerador()));
        DenC.setText(String.valueOf(C.getDenominador()));
        SignoC.setText(String.valueOf(C.getSigno()));
    }
    private void Operacion(String Operacion,Fraccion A, Fraccion B){
        
        if(Operacion.equals("Suma")){
            C.suma(A, B);
            AsignarValorFraccionC();
        }
        if(Operacion.equals("Resta")){
            C.resta(A, B);
            AsignarValorFraccionC();
        }
        if(Operacion.equals("Multiplicación")){
            C.multiplicacion(A, B);
            AsignarValorFraccionC();
        }
        if(Operacion.equals("División")){
            if(A.getNumerador()!=0 && B.getNumerador()!=0){
                C.division(A, B);
                AsignarValorFraccionC();
            }
            else{
                if(A.getNumerador()==0){
                    JOptionPane.showMessageDialog(rootPane, "Numerador de la Fraccion A es Invalido para Realizar una división");
                    NumA.setText("");
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Numerador de la Fraccion B es Invalido para Realizar una división");
                    NumB.setText("");
                }
            }
            
        }
        
    }

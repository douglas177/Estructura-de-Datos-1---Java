    //Proceso que habilita los botones para realizar la operacion de la fraccion
    private void Habilitar(){
        if(NumA.getText().length() != 0 &&NumB.getText().length() != 0 &&DenoA.getText().length() != 0 &&DenB.getText().length() != 0){
            Igual.setEnabled(true);
        }
        else{
            Igual.setEnabled(false);
            if(NumC.getText().length()!= 0)
                Limpiar();
        }
    }
    //Funcion que devuelve si el dato ingresado es numero
    private boolean EsNumero(String numero){
        return numero.matches("[0-9]*");
    }
    //limpia las casilla de texto
    private void Limpiar(){
        SignoC.setText("");
        NumC.setText("");
        DenC.setText("");
    }
    //Proceso que realiza la el evento al escribir alguna Valor el Numerador
    private void EventoNumerador(JTextField obj,  String fraccion){
        if(!EsNumero(obj.getText())){
            JOptionPane.showMessageDialog(rootPane, "El Numerador de la Fraccion "+fraccion+" es Invalido");
            obj.setText("");  
        }
        Habilitar();   
    }
    //Proceso que realiza la el evento al escribir alguna Valor el Denominador
    private void EventoDenominador(JTextField obj,  String fraccion){
        if(!EsNumero(obj.getText())||"0".equals(obj.getText())){
            JOptionPane.showMessageDialog(rootPane, "El Denominador de la Fraccion "+fraccion+" es Invalido");
            obj.setText("");  
        }
        Habilitar();   
    }
    
